/**
 * Get all Indian states sorted alphabetically
 */
export const getStates = (): string[] => {
    return Object.keys(INDIAN_STATES_CITIES).sort();
};

/**
 * Get all cities for a specific state, sorted alphabetically
 */
export const getCitiesByState = (stateName: string): string[] => {
    if (!stateName) return [];
    const cities = INDIAN_STATES_CITIES[stateName] || [];
    return cities.sort((a, b) => a.localeCompare(b));
};
